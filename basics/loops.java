public class loops {
    public static void main(String[] args) {
        int i=0;
        while(i<10){                    //while loop
            System.out.print(i+"\t");
            i++;
        }

        System.out.println();
        for(i=0;i<10;i++){              //for loop
            System.out.println(i+"\n");
        }
    }
}
